namespace Sfynx\CmfBundle\Layers\Infrastructure\Persistence\Repository;

use Doctrine\ORM\EntityRepository;
use Sfynx\CoreBundle\Layers\Infrastructure\Persistence\Adapter\Generalisation\Orm\Traits\TraitTranslation;

/**
 * KeyWord Repository
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 * 
 * @package    Cmf
 * @subpackage Repository
 * @author     Etienne de Longeaux <etienne.delongeaux@gmail.com>
 * @since      2012-01-06
 */
class KeyWordRepository extends EntityRepository
{
    use TraitTranslation;

    /**
     * Return all keyword.
     *
     * @return array
     * @access public
     * @author Etienne de Longeaux <etienne.delongeaux@gmail.com>
     * @since  2012-01-23
     */
    public function getAllPageKeyWords()
    {
        $query = $this->createQueryBuilder('k')
        ->select('k')
        ->where('k.enabled = :enabled')
        ->orderBy('k.groupname', 'ASC')
        ->setParameter('enabled', 1);
    
        return $query;
    }    
    
    /**
     * Gets all groupname of key word.
     *
     * @return array
     * @access public
     * @author Etienne de Longeaux <etienne.delongeaux@gmail.com>
     * @since  2012-03-28
     */
    public function getArrayAllGroupName()
    {
        $query = $this->createQueryBuilder('k')
        ->select('k.groupname')
        ->where('k.enabled = :enabled')
        ->setParameters(array(
                'enabled'    => 1,
        ));
        $result = array();
        $data    = $query->getQuery()->getArrayResult();
        if ($data && is_array($data) && count($data)) {
            foreach ($data as $row) {
                if (!empty($row['groupname']))
                    $result[ $row['groupname'] ] = $row['groupname'];
            }
        }
        
        return $result;
    }
}
